def get_equinox(y):
    rest = y % 100
    cent = y - rest
    cp = get_century_position(cent)
    rp = get_two_digits_position(rest)
    d, m = dates.iloc[rp, cp]
    return (y, m, d)

def get_two_digits_position(rest):
    count = 0
    for arr in decades:
        if rest in arr: return count
        count += 1
    raise IndexError('no such year')

def get_century_position(cent):
    count = 0
    for arr in centuries:
        if cent in arr: return count
        count += 1
    raise IndexError('no such century')

